public class ForLoops{
    public static void main(String[] args) {
        for(int i = 0; i <=4; i++){
            System.out.println(i);
        }
    }
}

/**
 * Use a 'for'loop whenever you know how any times a block of code needs to run
 * Use a 'while' loop when you need to run as long as the condition is still true
 */